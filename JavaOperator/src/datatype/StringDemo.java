public class StringDemo {
	public static void main(String[] args) {
		char c = 'c'; 
		// 맨 끝에 있는 ; = 세미컬럼 = 구분자 (마침표 역할)
		// 카 데이터 타입의 c 변수에 알파벳 c 값을 할당함
		System.out.println(c);
		String s = "abc";
		System.out.println(s);
		System.out.println("abc");
		// 알파벳 하나만 출력할 경우는 char 타입을 사용
		// 하나이상 복수의 문자열을 출력하 경우 String 타입을 사용한다.
		// 주의할점은 싱글쿼터와 더블쿼터에 유의
	}

}
